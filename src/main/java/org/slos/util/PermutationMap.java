    public static <T> List<T> getPermutation(List<T> list, int id) {
        if (list == null) {
            throw new IllegalStateException("Can not make permutation for null.");
        }
        if (list.size() == 1) {
            return list;
        }

        int listSize = list.size();
        int permutationMap[][] = getPermutationMap(listSize);
        int[] order = permutationMap[id];

        List<T> permutation = new ArrayList<>();
        for (int i = 0; i < listSize; i++) {
            permutation.add(list.get(order[i]));
        }

        return permutation;
    }

    public static int[][] getPermutationMap(int size) {
        switch (size) {
            case 2:
                return TWO;
            case 3:
                return THREE;
            case 4:
                return FOUR;
            case 5:
                return FIVE;
            case 6:
                return SIX;
             default:
                 throw new UnsupportedOperationException("Permutation size not supported: " + size);
        }
    }



